from telebot.types import Message

from loguru import logger

from loader import bot
from config_data.config import DEFAULT_COMMANDS


@bot.message_handler(commands=['help'])
def bot_help(message: Message) -> None:
    """
    –§—É–Ω–∫—Ü–∏—è, –¥–∞–µ—Ç —Å–ø—Ä–∞–≤–æ—á–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –ø–æ —Ä–∞–±–æ—Ç–µ –±–æ—Ç–∞

    –ê–∫—Ç–∏–≤–∏—Ä—É–µ—Ç—Å—è –≤ –º–æ–º–µ–Ω—Ç –Ω–∞–ø–∏—Å–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º /help.

    """
    logger.info(f'{message.chat.id} - –∫–æ–º–∞–Ω–¥–∞ help')
    text = [f'/{command} - {desk}' for command, desk in DEFAULT_COMMANDS]
    bot.send_message(message.chat.id,
                     '–Ø –±–æ—Ç, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–º–æ–∂–µ—Ç —Ç–µ–±–µ –Ω–∞–π—Ç–∏ –æ—Ç–µ–ª—å –¥–ª—è —Ç–≤–æ–µ–≥–æ –æ—Ç–¥—ã—Ö–∞.\n'
                     '\n–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –∏–∑-–∑–∞ –Ω–µ–∫–æ—Ç–æ—Ä—ã—Ö –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–π –ø–æ–∏—Å–∫ –ø–æ –†–æ—Å—Å–∏–∏ –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω\n'
                     '\nüîç–î–ª—è –ø–æ–∏—Å–∫–∞ –≥–æ—Ä–æ–¥–∞ –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ä—É—Å—Å–∫–∏–π –∏ –∞–Ω–≥–ª–∏–π—Å–∫–∏–π —è–∑—ã–∫\n'
                     '\n–ò—Å–ø–æ–ª—å–∑—É–π –º–æ–∏ –∫–æ–º–∞–Ω–¥—ã, —á—Ç–æ–±—ã –Ω–∞–π—Ç–∏ –ø–æ–¥—Ö–æ–¥—è—â–∏–π –æ—Ç–µ–ª—å:\n\n'
                     + '\n'.join(text))
